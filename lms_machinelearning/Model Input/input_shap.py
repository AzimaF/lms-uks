"""### Penjelasan Warna pada SHAP Waterfall Plot

Pada SHAP waterfall plot:

*   **Warna Merah:** Batang berwarna merah menunjukkan fitur-fitur yang **meningkatkan** probabilitas prediksi model. Dalam konteks klasifikasi (misalnya, Tuberculosis vs. Normal), fitur merah mendorong prediksi model *lebih dekat ke kelas positif* (misalnya, 'Tuberculosis'). Semakin panjang batangnya, semakin besar pengaruh positifnya.

*   **Warna Biru:** Batang berwarna biru menunjukkan fitur-fitur yang **menurunkan** probabilitas prediksi model. Fitur biru mendorong prediksi model *lebih dekat ke kelas negatif* (misalnya, 'Normal'). Semakin panjang batangnya, semakin besar pengaruh negatifnya.

Plot ini dimulai dari `base value` (nilai dasar, yaitu rata-rata prediksi model pada dataset). Kemudian, setiap batang (fitur) menambah atau mengurangi nilai tersebut sampai mencapai prediksi akhir model untuk input yang Anda berikan. Dengan demikian, Anda bisa melihat secara visual fitur mana yang paling berpengaruh dan ke arah mana pengaruhnya terhadap prediksi tertentu.
"""

