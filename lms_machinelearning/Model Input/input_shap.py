    if shap_values.ndim == 2:
        # For binary classification, shap_values is (n_samples, n_features)
        # We take the values for the first (and only) sample
        shap_values_for_plot = shap_values[0, :]
        # The expected value for binary classification might be a single value
        # or an array/list of two values. We pick the one corresponding to the predicted class.
